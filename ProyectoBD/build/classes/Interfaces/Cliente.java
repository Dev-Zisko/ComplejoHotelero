    private void IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarActionPerformed
        if(!textCedula.getText().equals("")&&!textNombre.getText().equals("")&&!textApellido.getText().equals("")&&!textTelefonoLocal.getText().equals("")&&!textTelefonoCelular.getText().equals("")&&!textTelefonoOficina.getText().equals("")&&!jDateChooser1.getDate().equals(null)&&!textProfesion.getText().equals("")&&!textCalle.getText().equals("")&& !textEdificio.getText().equals("")&&!textUrbanizacion.getText().equals("")&&!textNacionalidad.getText().equals("")){
        control.InsertarCliente(jYearChooser1.getYear(),0, textCedula.getText(), control.ValidarMayusculas(textNombre.getText()), textApellido.getText(), textTelefonoLocal.getText(), textTelefonoCelular.getText(), textTelefonoOficina.getText(), control.convertJavaDateToSqlDate(jDateChooser1.getDate()), textNacionalidad.getText(), ((String) comboSexo.getSelectedItem()).charAt(0), textProfesion.getText(), textInformacionGeneral.getText(), Integer.parseInt(control.ConsultaPrimaria("Codigo", "lugar_7", "Descripcion", (String) comboCiudad.getSelectedItem())), Integer.parseInt(control.ConsultaPrimaria("Codigo", "lugar_7", "Descripcion", (String) comboPaisOrigen.getSelectedItem())), Integer.parseInt(control.ConsultaPrimaria("Codigo", "hotel_7", "Nombre", (String) comboHotel.getSelectedItem())), textCalle.getText(), textEdificio.getText(), textUrbanizacion.getText());
        }
        else
            JOptionPane.showMessageDialog(null,"Datos requeridos invalidos, Verifique e intente nuevamente"); 
       
        textCedula.setText("");
        textNombre.setText("");
        textApellido.setText("");
        textTelefonoLocal.setText("");
        textTelefonoCelular.setText("");
        textTelefonoOficina.setText("");
        jDateChooser1.setDate(null);
        textProfesion.setText("");
        textCalle.setText("");
        textUrbanizacion.setText("");
        textEdificio.setText("");
        textNacionalidad.setText("");
        textInformacionGeneral.setText("");
        jYearChooser1.setYear(2017);
    }//GEN-LAST:event_IngresarActionPerformed

